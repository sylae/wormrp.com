namespace Handler;

class MapHandler
{

    public function respond(array $vars)
    {
        global $config;

        $loader = new \Twig\Loader\FilesystemLoader('tpl');
        $twig = new \Twig\Environment($loader, [
            'cache' => false,
        ]);

        $vars['session'] = $_SESSION;

        echo $twig->render("map.twig", $vars);
        // var_dump($_SESSION);
    }
}
